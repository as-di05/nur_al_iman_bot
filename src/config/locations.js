// Плоский список всех городов для быстрого поиска
export const ALL_CITIES = {};
Object.values(REGIONS).forEach((region) => {
  Object.entries(region.cities).forEach(([code, name]) => {
    ALL_CITIES[code] = { name, regionName: region.name };
  });
});

// Получить название города по коду
export function getCityName(code) {
  return ALL_CITIES[code]?.name || "Неизвестный город";
}

// Получить регион по коду города
export function getRegionName(code) {
  return ALL_CITIES[code]?.regionName || "Неизвестная область";
}
